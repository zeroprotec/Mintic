#saber si un número ingresado es positivo o negativo.

print("Ingrese un número:")
num = int(input())
if num ==0:
    #identacion o indentation, es la tabulacion a la derecha
    print("El numero ingresado es cero.")

elif (num>0):
    print("El numero ingresado es positivo")

else: 
    print("El numero ingresado es negativo.") 
